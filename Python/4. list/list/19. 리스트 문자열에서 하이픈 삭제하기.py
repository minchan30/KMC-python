# 리스트 문자열에서 하이픈('-') 삭제하기
phone_list1 = ['010-3523-1629' , '010-6276-0905' , '010-8663-1628' , '010-8345-3378']
print(phone_list1)

phone_list2 = []
for number in phone_list1 :
    x = number.replace('-' , "")

    phone_list2.append(x)

print(phone_list2)

# 실행결과
# ['010-3523-1629', '010-6276-0905', '010-8663-1628', '010-8345-3378']
